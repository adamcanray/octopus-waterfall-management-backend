const mongoose = require('mongoose')

const AdminSchema = mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  first_name: {
    type: String,
    required: true,
  },
  last_name: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  created_at: {
    type: Date,
    default: Date.now()
  }
})

const Admin = mongoose.model('admin', AdminSchema)
Admin.createIndexes()
module.exports = Admin